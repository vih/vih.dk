<h1>Politi</h1>

<h2>Drømmer du om at komme ind til politiet?</h2>

<ul>
    <li>Men du er ikke klar til de udfordringer, som Politiets optagelsesprøve byder på?</li>
    <li>Er du i tvivl om Politiet overhovedet er noget dig, og derfor vil du gerne have en lille smagsprøve på, hvad jobbet kræver og består af?</li>
    <li>Vil du gerne opleve et højskoleophold, hvor sociale kompetencer, empati og samarbejde udvikles?</li>
</ul>

<p>Ja, så kunne et ophold på Vejle Idrætshøjskole være noget for dig!</p>

<p>Vi har stor erfaring med fysisk træning. Vi prøver altid med udgangspunkt i din fysiske tilstand at hjælpe dig mod succes inden for optagelsesprøvens udfordringer. Du bliver præsenteret for programmer til løb og styrketræning. Vi arbejder med svømning, og du har mulighed for at tage bassinprøven.</p>

<p>Vi arbejder med dansk grammatik og fremstilling samt engelsk, som siden foråret 2010 er blevet en del af de boglige optagelsesprøver. Det skal dog understeges, at den boglige undervisning vil være af repeterende karakter.</p>

<p>Vi vil også gerne under opholdet prøve at give dig et indblik i livet som politibetjent. Et godt samarbejde med Vejle Politi, Politiskolen i Brøndby, samt politiassistenter, hjælper hvert semester med at give dig indblik i politiets hverdag. Du møder også tidligere elever på Vejle Idrætshøjskole, der er kommet ind til politiet.</p>